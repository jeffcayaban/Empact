package com.empact.Empact.model;

/**
 * The different critical questions that the user can answer in order to create an argument or petition.
 */

public enum CriticalQuestion {
	CQ1,	// Are the circumstances true?
	CQ2,	// Does the action achieve the goal?,

	EO_CQ1,	// Is the expert credible?,
	PO_CQ1 // Is the explanation generally accepted?
}
